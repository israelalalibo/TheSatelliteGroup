/**
 * Consolidated CSV products - one card per product group with size/type options.
 * Images stored locally in public/images/products/ (run scripts/download-product-images.js)
 */
function img(imageFile: string): string {
  const ext = imageFile.includes(".") ? imageFile.substring(imageFile.lastIndexOf(".")) : ".jpg";
  const base = imageFile.replace(/\.[^.]+$/, "").replace(/_/g, "-").toLowerCase();
  return `/images/products/${base}${ext}`;
}

